package ca.kaxx.board.packets;

/**
 * This enum represents the actions that can be performed on a scoreboard.
 */
public enum ScoreboardAction {

    /**
     * The CREATE variable represents the action of creating a scoreboard.
     */
    CREATE,
    /**
     * Represents the DELETE action that can be performed on a scoreboard.
     * DELETE action is used to delete a scoreboard.
     */
    DELETE,
    /**
     * Represents a scoreboard update action.
     *
     * This enum is used to specify the action that needs to be performed on a scoreboard.
     */
    UPDATE

}
